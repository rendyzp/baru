    public function index2()
    {
        $kelas = Kelas::all();
        $data = [];
        foreach ($kelas as $kelas) {
            $siswa = Siswa::where('kelas', $kelas->namaKelas)->get();
            $data[$kelas->namaKelas] = $siswa;
        }
        return $data;
    }

